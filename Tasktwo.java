//2)	using switch ….case   display whether character is vowel or not.

public class Tasktwo {
	public static void main(String[] args) {
		char letter = 'k';
		switch (letter) {
		case 'a':
			System.out.println("This is a Vowel");
			break;
		case 'e':
			System.out.println("This is a Vowel");
			break;
		case 'i':
			System.out.println("This is a Vowel");
			break;
		case 'o':
			System.out.println("This is a Vowel");
			break;
		case 'u':
			System.out.println("This is a Vowel");
			break;
		default:
			System.out.println("This is NOT a Vowel");

		}
	}
}
